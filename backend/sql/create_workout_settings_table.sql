    preview_video_cycle_code VARCHAR(50) DEFAULT NULL COMMENT 'preview Video Cycle',
    execution_video_cycle_code VARCHAR(50) DEFAULT NULL COMMENT 'execution Video Cycle',
    status ENUM('DRAFT', 'ENABLED', 'DISABLED') NOT NULL DEFAULT 'DRAFT' COMMENT '状态',
    is_deleted TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否删除',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    INDEX idx_status (status),
    INDEX idx_is_deleted (is_deleted)
) COMMENT='训练设置表';
